            Here are the steps to purchase tickets on the Bruno On Stage (BOS) website: 

1. Visit the BOS Website:
- Open your browser and go to the official BOS website. 

2. Select the Bruno Mars Concert: 
- Find and click on the Bruno Mars concert event on the homepage. 

3. Choose Your Ticket Category:
- Select your preferred ticket category from the available options. 

4. Specify the Number of Tickets:
- Choose the number of tickets you want to purchase. 

5. Click "Buy Now": 
- After selecting the category and quantity, click the "Buy Now" button. 

6. Enter Your Details:
- Fill in your personal details, such as name, email, and phone number, required for the booking. 

7. Select Payment Method:
- Choose your preferred payment method, such as credit card, bank transfer, or e-wallet. 

8. Confirm Payment:
- Follow the instructions to complete the payment and confirm your purchase. 

9. Receive E-Ticket: 
- Once the payment is successful, your e-ticket will be sent to your email. Keep this e-ticket for entry to the event. 

10. Print or Save Your E-Ticket:
- You can either print your e-ticket or save it on your phone to show at the entrance. 

These steps will help you easily and quickly purchase your tickets on the BOS website.

